package com.company.books.backend.examples.junit;

public class Calculator {

    float add(float a, float b) {
        return a + b;
    }

    float sub(float a, float b) {
        return a - b;
    }

    float mul(float a, float b) {
        return a * b;
    }
    
    float div(float a, float b) {
        return a / b;
    }
    
}
